package BOONGTOLJAVA.b_7_6_package1;

public class A {
    //필드 선언
	protected String field;

	//생성자 선언
	protected A() {
	}

	//메소드 선언
	protected void method() {
	}
}

// 자바에서 모든 멤버(member)는 반드시 클래스나 객체에 속합니다.
// 현재 클래스의 객체 obj 에 대해 obj.field , obj.method 와 같이 field 와 method 에 접근하는것	vs 자식 클래스에서 부모 클래스의 객체를 만들고 접근하는 것은 서로 다른 접근 규칙을 갖는다!
